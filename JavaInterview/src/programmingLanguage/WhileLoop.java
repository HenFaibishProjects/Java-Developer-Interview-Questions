package programmingLanguage;

public class WhileLoop {

    public void simpleWhileLoop() {
        int i = 0;
        while (i < 5) {
            System.out.println(i);
            i++;
        }
    }

    public void doWhileLoop() {
        int i = 0;
        do {
            System.out.println(i);
            i++;
        }
        while (i < 5);
        }

}
